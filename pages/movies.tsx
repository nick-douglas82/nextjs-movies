import Head from 'next/head'
import { InferGetStaticPropsType } from 'next'
import { Data, dataMutation } from '../lib/helpers/dataMutation'
import List from '../components/List'
import SiteHeader from '../components/SiteHeader'

const Movies = ({ movies }: InferGetStaticPropsType<typeof getStaticProps>) => {
  return (
    <div>
      <Head>
        <title>NextJs Movies</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="mx-auto mt-10 max-w-7xl">
        <SiteHeader />
        <List title="Popular movies" listData={movies} />
      </main>
    </div>
  )
}

export async function getStaticProps() {
  const results = await fetch('https://api.themoviedb.org/3/movie/popular?api_key=e0c577647a14eae09f07aa14fee7caeb&page=1')
  const moviesData = await results.json();

  const movies = moviesData.results.map((data: Data) => dataMutation(data, "movie"));

  return {
    props: {
      movies
    }
  }
}

export default Movies